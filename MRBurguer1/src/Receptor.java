    public void idIncrementable() {
        int j;
        String c = "";
        String qrySelect = "Select max(num_orden) AS num_orden from pedido ";
        ResultSet resultado;
        String id = "";
        int orden = 0;
        int cont = 1;
        int cant;

        try {
            sentencia = conectar.createStatement();
            resultado = sentencia.executeQuery(qrySelect);
            if (resultado.next()) {
                c = resultado.getString("num_orden");
            }
            if (c == null) {
                txtNum_Orden.setText("1");
            } else {
                orden = Integer.parseInt(c);
                char r1 = c.charAt(0);
                String juntar = "" + r1;
                int var = Integer.parseInt(juntar);
                cant = cont + var;
                txtNum_Orden.setText("" + cant);

                if (orden > 9) {
                    r1 = c.charAt(0);
                    char r2 = c.charAt(1);
                    juntar = "" + r1 + r2;
                    var = Integer.parseInt(juntar);
                    cant = cont + var;
                    txtNum_Orden.setText("" + cant);
                }
                if (orden > 99) {
                    r1 = c.charAt(0);
                    char r2 = c.charAt(1);
                    char r3 = c.charAt(2);
                    juntar = "" + r1 + r2 + r3;
                    var = Integer.parseInt(juntar);
                    cant = cont + var;
                    txtNum_Orden.setText("" + cant);
                }
                if (orden > 999) {
                    r1 = c.charAt(0);
                    char r2 = c.charAt(1);
                    char r3 = c.charAt(2);
                    char r4 = c.charAt(3);
                    juntar = "" + r1 + r2 + r3 + r4;
                    var = Integer.parseInt(juntar);
                    cant = cont + var;
                    txtNum_Orden.setText("" + cant);
                }
            }
        } catch (Exception e) {
            mensaje("Error con el numero de orden");
        }

    }

    public void MostrarFecha() {
        this.Tabla.setModel(modelo);
        //Asignando a los campos de texto de dia y mes la fecha actual de cada dia
        Calendar cal = Calendar.getInstance();
        String fecha;
        String mes;
        if (cal.get(Calendar.MONTH) < 10) {
            fecha = cal.get(Calendar.YEAR) + "/" + "0" + ((cal.get(Calendar.MONTH)) + 1) + "/" + cal.get(Calendar.DATE);
            this.txtFecha.setText(fecha);

        } else {
            fecha = cal.get(Calendar.YEAR) + "/" + ((cal.get(Calendar.MONTH)) + 1) + "/" + cal.get(Calendar.DATE);
            this.txtFecha.setText(fecha);
        }

    }

